    private final String name;
    private final String caption;
    private final Color color;

    FluentColor(String name, String caption, Color color) {
        this.name = name;
        this.caption = caption;
        this.color = color;
    }

    public static FluentColor parse(String name) {
        return Stream.of(values()).filter(e -> e.name().equals(name)).findFirst().orElse(null);
    }

    public String getName() {
        return name;
    }

    public String getCaption() {
        return caption;
    }

    public Color getColor() {
        return color;
    }

    public String hex() {
        return '#' + Integer.toHexString(color.getRGB());
    }

    public String hex(int a) {
        return "#"
                + Integer.toHexString(color.getRed())
                + Integer.toHexString(color.getGreen())
                + Integer.toHexString(color.getBlue())
                + Integer.toHexString(a);
    }

    public String rgb() {
        return "rgb(" + color.getRed() + ", " + color.getGreen() + ", " + color.getBlue() + ")";
    }

    public String rgba(int a) {
        return "rgba(" + color.getRed() + ", " + color.getGreen() + ", " + color.getBlue() + ", " + a + ")";
    }

    @Override
    public String toString() {
        return name();
    }
